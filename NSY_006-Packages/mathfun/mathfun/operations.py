def add(x,y):
    """
    Purpose: Add two numbers
    Parameters:
    x : Integer
    y : Integer
    Return: Returns sum of two integers x and y

    """
    return x + y


def subtract(x,y):
    """
    Purpose: Subtract two numbers
    Parameters:
    x : Integer
    y : Integer
    Return: Returns Subtraction of two integers x and y
    
    """
    return x - y


def multiply(x,y):
    """
    Purpose: Mutiply two numbers
    Parameters:
    x : Integer
    y : Integer
    Return: Returns Product of two integers x and y
    
    """    
    return x * y

